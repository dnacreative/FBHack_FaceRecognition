package src.FBHack;

public class Person {

	private String name;
	private int upperLeftX;
	private int upperLeftY;
	private int lowerRightX;
	private int lowerRightY;
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the upperLeftX
	 */
	public int getUpperLeftX() {
		return upperLeftX;
	}
	/**
	 * @param upperLeftX the upperLeftX to set
	 */
	public void setUpperLeftX(int upperLeftX) {
		this.upperLeftX = upperLeftX;
	}
	/**
	 * @return the upperLeftY
	 */
	public int getUpperLeftY() {
		return upperLeftY;
	}
	/**
	 * @param upperLeftY the upperLeftY to set
	 */
	public void setUpperLeftY(int upperLeftY) {
		this.upperLeftY = upperLeftY;
	}
	/**
	 * @return the lowerRightX
	 */
	public int getLowerRightX() {
		return lowerRightX;
	}
	/**
	 * @param lowerRightX the lowerRightX to set
	 */
	public void setLowerRightX(int lowerRightX) {
		this.lowerRightX = lowerRightX;
	}
	/**
	 * @return the lowerRightY
	 */
	public int getLowerRightY() {
		return lowerRightY;
	}
	/**
	 * @param lowerRightY the lowerRightY to set
	 */
	public void setLowerRightY(int lowerRightY) {
		this.lowerRightY = lowerRightY;
	}
	
	
	
	
	
}
